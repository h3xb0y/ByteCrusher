using ByteCrusher.Addition;
using ByteCrusher.Entities;

namespace PingPong.Drawer
{
  public class MenuBackgroundDrawer : IEntityDrawer
  {
    private const string _code =
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n" +
      "___________________________________________________________________________________________________\n";

    private readonly AsciiCode _asciiCode;

    public MenuBackgroundDrawer()
      => _asciiCode = new AsciiCode()
        .AddColor("*", "250", "20")
        .AddColor("_", "250", "10", true);

    public string Code()
      => _code;

    public string Replace(string element)
      => _asciiCode
        .AddDrawing(element)
        .Build();
  }
}